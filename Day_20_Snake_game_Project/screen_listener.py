
# Move Left
def head_north(a_turtle):
    a_turtle.setheading(90)
    a_turtle.forward(10)


def head_east(a_turtle):
    a_turtle.setheading(0)
    a_turtle.forward(10)


def head_west(a_turtle):
    a_turtle.setheading(180)
    a_turtle.forward(10)


def head_south(a_turtle):
    a_turtle.setheading(270)
    a_turtle.forward(10)




